    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        btnRemove.setBackground(Color.GRAY);
        btnRemove.setEnabled(false);
        if (!txtQuantity.getText().equals("") && !lblIdProduct.getText().equals("")) {
            SQLiteJDBC sqlite = new SQLiteJDBC();
            String token = sqlite.getToken();

            ProductsSaleRequest product = new ProductsSaleRequest();

            product.setProductId(Integer.valueOf(lblIdProduct.getText()));
            product.setQuantity(Integer.valueOf(txtQuantity.getText()));

            Call<ResponseBody> restockResponseCall = ApiClient.getProductService().removeStock(product, "Bearer " + token);
            restockResponseCall.enqueue(new Callback<ResponseBody>() {
                @Override
                public void onResponse(Call<ResponseBody> call, Response<ResponseBody> response) {
                    if (response.isSuccessful()) {
                        JOptionPane.showMessageDialog(null, "Se quit√≥ " + txtQuantity.getText() + " productos a " + lblName.getText() + " exitosamente", "Quitar inventario", JOptionPane.INFORMATION_MESSAGE);
                        btnRemove.setBackground(new java.awt.Color(0, 166, 237));
                        btnRemove.setEnabled(true);
                        txtCodeProduct.setText("");
                        txtQuantity.setText("");
                        lblIdProduct.setText("");
                        lblName.setText("-");
                        lblStock.setText("-");
                    }
                } 

                @Override
                public void onFailure(Call<ResponseBody> call, Throwable t) {
                    System.out.println(t.getLocalizedMessage());
                }
            });
        } else {
            btnRemove.setBackground(new java.awt.Color(0, 166, 237));
            btnRemove.setEnabled(true);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

