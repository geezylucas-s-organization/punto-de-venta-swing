    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        btnSave.setBackground(Color.GRAY);
        btnSave.setEnabled(false);
        if (!txtName.getText().equals("")) {
            SQLiteJDBC sqlite = new SQLiteJDBC();
            String token = sqlite.getToken();
            
            AddCategoryRequest addCategory = new AddCategoryRequest();
            addCategory.setName(txtName.getText());
            addCategory.setDescription(txtDescription.getText());
            Call<ResponseBody> addCategoryResponseCall = ApiClient.getCategoryService().addCategory(addCategory, "Bearer " + token);
            addCategoryResponseCall.enqueue(new Callback<ResponseBody>() {
                @Override
                public void onResponse(Call<ResponseBody> call, Response<ResponseBody> response) {
                    if (response.isSuccessful()) {
                        JOptionPane.showMessageDialog(null, "Departamento " + txtName.getText() + " creado exitosamente", "Crear departamento", JOptionPane.INFORMATION_MESSAGE);
                        btnSave.setBackground(new java.awt.Color(0, 166, 237));
                        btnSave.setEnabled(true);
                        txtName.setText("");
                        txtDescription.setText("");
                    }
                }
                
                @Override
                public void onFailure(Call<ResponseBody> call, Throwable t) {
                    System.out.println(t.getLocalizedMessage());
                }
            });
        } else {
            JOptionPane.showMessageDialog(null, "El nombre del departamento es obligatorio", "Crear departamento", JOptionPane.ERROR_MESSAGE);
            btnSave.setBackground(new java.awt.Color(0, 166, 237));
            btnSave.setEnabled(true);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

