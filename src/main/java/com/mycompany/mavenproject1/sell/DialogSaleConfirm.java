    public DialogSaleConfirm(java.awt.Frame parent, boolean modal, SaleRequest saleRequest) {
        super(parent, modal);
        this.saleRequest = saleRequest;
        initComponents();
        myInitComponents();
        txtMoney.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void changedUpdate(DocumentEvent e) {
                warn();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                warn();
            }

            @Override
            public void insertUpdate(DocumentEvent e) {
                warn();
            }

            public void warn() {
                try {
                    if (Double.parseDouble(txtMoney.getText()) <= 0) {
                        JOptionPane.showMessageDialog(null,
                                "Error: Please enter number bigger than 0", "Error Massage",
                                JOptionPane.ERROR_MESSAGE);
                    } else if (Double.parseDouble(txtMoney.getText()) >= Double.parseDouble(lblTotalMoney.getText().substring(1))) {
                        Double money = Double.valueOf(txtMoney.getText());
                        Double totalMoney = Double.valueOf(lblTotalMoney.getText().substring(1));
                        lblReturned.setText(String.format("$%,.2f", (money - totalMoney)));
                    }
                } catch (NumberFormatException e) {
                    lblReturned.setText("-");
                }
            }
        });
    }

    private void myInitComponents() {
        lblTotalMoney.setText("$" + this.saleRequest.getTotal());
    }

