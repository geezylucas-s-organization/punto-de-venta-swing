    private void btnPayWithoutTicketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPayWithoutTicketActionPerformed
        // TODO add your handling code here:
        btnPayWithoutTicket.setBackground(Color.GRAY);
        btnPayWithoutTicket.setEnabled(false);

        if (!txtMoney.getText().equals("")) {
            SQLiteJDBC sqlite = new SQLiteJDBC();
            String token = sqlite.getToken();
            this.saleRequest.setCash(new BigDecimal(txtMoney.getText()));

            Call<ResponseBody> saleResponseCall = ApiClient.getSalesService().saleProducts(saleRequest, "Bearer " + token);
            try {
                saleResponseCall.execute();
            } catch (IOException ex) {
                Logger.getLogger(DialogSaleConfirm.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

        this.dispose();
    }//GEN-LAST:event_btnPayWithoutTicketActionPerformed

